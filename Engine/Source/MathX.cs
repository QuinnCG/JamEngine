namespace Engine;

/// <summary>
/// A math class that supports both floats and integers.
/// </summary>
public static class MathX
{
	public static float Sin(float x)
	{
		return MathF.Sin(x);
	}
	public static float Cos(float x)
	{
		return MathF.Cos(x);
	}
	public static float Tan(float x)
	{
		return MathF.Tan(x);
	}
}
